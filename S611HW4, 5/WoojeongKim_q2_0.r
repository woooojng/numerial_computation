> update_sampler <- function(sampler, iterations = 1){
     n <- sampler$iteration
     for (i in seq(from = n, length = iterations)){
         super$update()
         if (i <= sampler$buffer_length){
             sampler$buffer_mu[i] <- sampler$buffer_mu
             sampler$buffer_s2[i] <- sampler$buffer_s2
         }
     }
     if (i > sampler$buffer_length) warning('Buffer full! extra samples discarded')
 }
